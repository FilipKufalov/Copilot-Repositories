// Console Logs are for testing purposes

// Function for plus
function plus(a,b){
    return a+b;
}
console.log("Calculation for 5+5=" + plus(5,5));

// Function for minus
function minus(a,b){
    return a-b;
}
console.log("Calculation for 5-5=" + minus(5,5));

// Function for multiply
function multiply(a,b){
    return a*b;
}
console.log("Calculation for 5*5=" + multiply(5,5));

// Function for divide
function divide(a,b){
    return a/b;
}
console.log("Calculation for 5/5=" + divide(5,5));

// Function for modulus
function modulus(a,b){
    return a%b;
}
console.log("Calculation for 5%5=" + modulus(10,2));

// Function for exponent
function exponent(a,b){
    return Math.pow(a,b);
}
console.log("Calculation for 5^5=" + exponent(5,5));

// Function for square root
function squareRoot(a){
    return Math.sqrt(a);
}
console.log("Calculation for square root of 5=" + squareRoot(5));

// Function for square
function square(a){
    return Math.pow(a,2);
}
console.log("Calculation for square of 4=" + square(4));

// Function for cube
function cube(a){
    return Math.pow(a,3);
}
console.log("Calculation for cube of 5=" + cube(5));